int isalnum(int c)
{
    return attrBitmap[c & 0x7F] & (IS_ALPHA | IS_DIGIT);
}

int isalpha(int c)
{
    return attrBitmap[c & 0x7F] & IS_ALPHA;
}

int iscntrl(int c)
{
    return attrBitmap[c & 0x7F] == 0;
}

int isdigit(int c)
{
    return attrBitmap[c & 0x7F] & IS_DIGIT;
}

